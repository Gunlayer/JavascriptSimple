/**
 * 
 * @param {number} num1 
 * @param {number} num2 
 * @returns sum
 */
function addition (num1, num2) {
    return num1 + num2
}

function subtraction (num1, num2) {
    return num1 - num2
}

function multiplication (num1, num2) {
    return num1 * num2
}

function division (num1, num2) {
    return num1 / num2
}

